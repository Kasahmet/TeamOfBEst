package takhir.week02;

public class Swap {
    public static void main(String[] args) {
        //swap two variables values without using a third variable
        int x = 10;
        int y = 15;

        x = y; //x = 15
        y = 10;


        System.out.println("x=" + x + " \n" + "y=" + y);
    }
}
